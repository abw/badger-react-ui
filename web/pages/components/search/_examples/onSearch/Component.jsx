import { Search } from '@/src/index.jsx'
import { sleep } from '@abw/badger-utils'

/* START */
import React from 'react'
// PRETEND: import { Search } from '@abw/badger-react-ui

const animals = [
  'Alan Aardvark',
  'Amy Aardvark',
  'Anne Aardvark',
  'Bart Badger',
  'Beatrice Badger',
  'Belinda Badger',
  'Ben Badger',
  'Bert Badger',
  'Betty Badger',
  'Bobby Badger',
  'Brenda Badger',
  'Brian Badger',
  'Brook Badger',
  'Buzz Badger',
]

const OnSearchExample = () => {
  // emulate an asynchronous API call
  const onSearch = async input =>
    sleep(500).then(
      () => {
        if (input.match(/throw/)) {
          throw 'You asked me to throw an error'
        }
        return animals.filter(
          animal =>
            animal
              .toLowerCase()
              .indexOf(input.toLowerCase()) !== -1
        )
      }
    )

  return (
    <Search onSearch={onSearch} debug/>
  )
}

export default OnSearchExample